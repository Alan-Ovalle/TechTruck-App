class DatosUnidad {
  final String? numeroEconomico;
  final String? marca;
  final String? modelo;
  final String? motor;
  final String? placas;
  final String? kilometros;
  final String? horasMotor;
  final String? tipo;
  final String? serieMotor;
  final String? year;
  final String? vin;

  const DatosUnidad({
    required this.numeroEconomico,
    required this.marca,
    required this.modelo,
    required this.motor,
    required this.placas,
    required this.kilometros,
    required this.horasMotor,
    required this.tipo,
    required this.serieMotor,
    required this.year,
    required this.vin,
  });
}
